def generate_word_files(list_words: str, boiler_plate_phrase: str, lang: str):
    for idx, word in enumerate(list_words):
        with open(f"words/{lang}/{word}.txt", "w") as f:
            f.write(boiler_plate_phrase.format(word=word))


